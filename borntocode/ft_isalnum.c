#include "libft.h"

int	ft_isalnum(int c)
{
	if ('a' <= c && c <= 'z')
		return (c);
	if ('A' <= c && c <= 'Z')
		return (c);
	if ('0' <= c && c <= '9')
		return (c);
	return (0);
}
